#include "printf.h"

void	nbr_hex(long n)
{
	if (n >= 16)
		nbr_hex(n / 16);
	if (n % 16 < 10)
		ft_putchar((n % 16) + 48);
	else
		ft_putchar((n % 16) - 10 + 97);
}
