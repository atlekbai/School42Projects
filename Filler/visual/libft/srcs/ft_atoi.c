int		ft_atoi(char const *str)
{
	int	num;
	int	sign;

	num = 0;
	sign = 1;
	while ((*str == '\n') || (*str == '\t') || (*str == '\v')
			|| (*str == ' ') || (*str == '\f') || (*str == '\r'))
		str++;
	if (*str == '-' && *(str + 1) >= '0' && *(str + 1) <= '9')
	{
		sign = -1;
		str++;
	}
	else if (*str == '+' && *(str + 1) >= '0' && *(str + 1) <= '9')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		num = num * 10 + *str - '0';
		str++;
	}
	return (num * sign);
}
