#include "printf.h"

int		ft_strdigit(char *str)
{
	int i;

	if (str == NULL)
		return (0);
	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] < '0' || str[i] > '9') && str[i] != '-' && str[i] != '+')
			return (0);
		if ((str[i] == '-' || str[i] == '+') && !str[i + 1])
			return (0);
		i++;
	}
	return (1);
}
